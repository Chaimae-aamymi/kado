CARD_COLORS = ["purple", "blue", "green", "amber", "pink"]


def get_lang():
    return session.get("lang", "fr")


def get_theme():
    return session.get("theme", "dark")


def is_logged_in():
    return "user_id" in session


def generate_gifts(age, gender, interests, budget, lang, currency="EUR", api_key_override=None):
    key = api_key_override or os.environ.get("GEMINI_API_KEY")
    if not key:
        return None, "Clé API Gemini manquante."
    
    genai.configure(api_key=key)
    # Using 'gemini-flash-latest' to match the 2026 futuristic environment
    model = genai.GenerativeModel("gemini-flash-latest")
    t = TRANSLATIONS[lang]
    prompt = t["gemini_prompt"](age, gender, interests, budget, currency)

    try:
        response = model.generate_content(prompt)
        raw = response.text.strip()
        raw = re.sub(r"^```(?:json)?\n?", "", raw)
        raw = re.sub(r"\n?```$", "", raw)
        gifts = json.loads(raw)
        for i, gift in enumerate(gifts):
            gift["color"] = CARD_COLORS[i % len(CARD_COLORS)]
            query = gift.get("image_query", gift["nom"]).replace(" ", "").replace('"', '').replace("'", "")
            gift["image_url"] = f"https://loremflickr.com/600/400/{query}"
        return gifts, None
    except Exception as e:
        return None, str(e)


def base_context():
    lang = get_lang()
    return {
        "t": TRANSLATIONS[lang],
        "lang": lang,
        "theme": get_theme(),
        "langs": [("fr", "🇫🇷 FR"), ("en", "🇬🇧 EN"), ("ar", "🇸🇦 AR")],
        "has_key": bool(session.get("api_key") or os.environ.get("GEMINI_API_KEY")),
        "user_id": session.get("user_id"),
        "user_name": session.get("user_name"),
        "is_logged_in": is_logged_in()
    }


# ── Routes ────────────────────────────────────────────────────────────────────

@app.route("/set-api-key", methods=["POST"])
def set_api_key():
    key = request.form.get("api_key", "").strip()
    if key:
        session["api_key"] = key
    return redirect(request.referrer or url_for("index"))


@app.route("/set-lang/<lang>")
def set_lang(lang):
    if lang in TRANSLATIONS:
        session["lang"] = lang
    return redirect(request.referrer or url_for("index"))


@app.route("/set-theme/<theme>")
def set_theme(theme):
    if theme in ("dark", "light"):
        session["theme"] = theme
    return redirect(request.referrer or url_for("index"))


@app.route("/", methods=["GET"])
def index():
    ctx = base_context()
    # Read from session for state persistence
    return render_template("index.html", errors={}, api_error=None,
                           old_age=session.get("age", ""), 
                           old_gender=session.get("gender", ""), 
                           old_interets=session.get("interets", ""), 
                           old_budget=session.get("budget", ""), 
                           old_currency=session.get("currency", "EUR"), **ctx)


# ── Auth Routes ───────────────────────────────────────────────────────────────

@app.route("/login", methods=["GET", "POST"])
def login():
    if request.method == "POST":
        username = request.form.get("username", "").strip().lower()
        password = request.form.get("password", "").strip()
        user = database.login_user(username, password)
        if user:
            session["user_id"] = user["id"]
            session["user_name"] = user["username"]
            return redirect(url_for("index"))
        return render_template("auth.html", error="Identifiants invalides", mode="login", **base_context())
    return render_template("auth.html", mode="login", **base_context())


@app.route("/register", methods=["GET", "POST"])
def register():
    if request.method == "POST":
        username = request.form.get("username", "").strip().lower()
        password = request.form.get("password", "").strip()
        if not username or not password:
            return render_template("auth.html", error="Veuillez remplir tous les champs", mode="register", **base_context())
        if database.register_user(username, password):
            return redirect(url_for("login"))
        return render_template("auth.html", error="Nom d'utilisateur déjà pris", mode="register", **base_context())
    return render_template("auth.html", mode="register", **base_context())


@app.route("/logout")
def logout():
    session.pop("user_id", None)
    session.pop("user_name", None)
    return redirect(url_for("login"))


