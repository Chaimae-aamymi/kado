@app.route("/generate", methods=["POST", "GET"])
def generate():
    lang = get_lang()
    t = TRANSLATIONS[lang]
    ctx = base_context()

    if request.method == "POST":
        age = request.form.get("age", "").strip()
        gender = request.form.get("gender", "").strip()
        interets = request.form.get("interets", "").strip()
        budget = request.form.get("budget", "").strip()
        currency = request.form.get("currency", "").strip()

        # Update session for persistence
        session["age"] = age
        session["gender"] = gender
        session["interets"] = interets
        session["budget"] = budget
        session["currency"] = currency

        errors = {}
        if not age or not age.isdigit() or not (1 <= int(age) <= 120):
            errors["age"] = t["error_age"]
        if not gender or gender not in ["homme", "femme"]:
            errors["gender"] = t["error_gender"]
        if not budget or not budget.replace(".", "").isdigit() or float(budget) <= 0:
            errors["budget"] = t["error_budget"]
        if not currency or currency not in ["DH", "EUR", "USD"]:
            errors["currency"] = t["error_currency"]
        if not interets:
            errors["interets"] = t["error_interets"]

        if errors:
            return render_template("index.html", errors=errors, api_error=None,
                                   old_age=age, old_gender=gender, old_interets=interets, 
                                   old_budget=budget, old_currency=currency, **ctx)

        gender_label = t["gender_man"] if gender == "homme" else t["gender_woman"]
        gifts, error = generate_gifts(int(age), gender_label, interets, float(budget), lang, 
                                      currency=currency, api_key_override=session.get("api_key"))

        if error:
            return render_template("index.html", api_error=error, errors={},
                                   old_age=age, old_gender=gender, old_interets=interets, 
                                   old_budget=budget, old_currency=currency, **ctx)

        session["gifts"] = gifts
    else:
        # GET request: load from session (useful after theme/lang change)
        age = session.get("age")
        gender = session.get("gender")
        interets = session.get("interets")
        budget = session.get("budget")
        currency = session.get("currency")
        gifts = session.get("gifts")

        if not gifts:
            return redirect(url_for("index"))

    if is_logged_in():
        user_id = session["user_id"]
        for gift in gifts:
            gift["is_favorite"] = database.is_favorite(user_id, gift["nom"])

    return render_template("results.html", gifts=gifts,
                           age=age, gender=gender, interets=interets, 
                           budget=budget, currency=currency, **ctx)


