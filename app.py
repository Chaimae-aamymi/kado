@app.route("/toggle-favorite", methods=["POST"])
def toggle_favorite():
    if not is_logged_in():
        return jsonify({"success": False, "error": "Veuillez vous connecter"}), 401
    
    gift_name = request.json.get("gift_name")
    gift_data = request.json.get("gift_data")
    
    user_id = session["user_id"]
    if database.is_favorite(user_id, gift_name):
        database.remove_favorite(user_id, gift_name)
        return jsonify({"success": True, "action": "removed"})
    else:
        database.add_favorite(user_id, gift_name, json.dumps(gift_data))
        return jsonify({"success": True, "action": "added"})


